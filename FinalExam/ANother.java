package FinalExam;

public class ANother {
    public int getMaxMagic(int level){
        if (level == 1) {
            return 10;
        }
        else if (level == 2) {
            return 15;
        }
        else if (level == 3) {
            return 20;
        }
        else if (level == 4) {
            return 25;
        }
        else if (level == 5) {
            return 30;
        }
        else if (level == 6) {
            return 35;
        }
        else if (level == 7) {
            return 40;
        }
        else if (level == 8) {
            return 50;
        }

    return level;
    }

}
